    public void atualizarListagem() throws IOException{
        String listagem = "";
        List<Quarto> quartos = gerente.getQuartos();
        for(Quarto quarto : quartos){
            listagem = listagem+quarto.toString() +"\n";
        }
        jTextArea1.setText(listagem);
        System.out.println(quartos);
        atualizarQuartosNoArquivo(quartos);
        
    }
    public void atualizarQuartosNoArquivo(List<Quarto> listaAtualizada) throws IOException{
        SerializadorQuarto serializador = new SerializadorQuarto();
        String  listaAtualizadaCSV = serializador.toCSV(listaAtualizada);
        FilePersistence atualizarQuartos = new FilePersistence();
        atualizarQuartos.saveToFile(listaAtualizadaCSV, "Quartos.txt");
}
