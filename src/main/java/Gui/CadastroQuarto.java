    public GerenciadorQuarto buscarGerente() throws FileNotFoundException{
        FilePersistence file = new FilePersistence();
        String arquivoLidoReservas = file.loadFromFile("Quartos.txt");
        SerializadorQuarto serializador = new SerializadorQuarto();
        List<Quarto> quartos = serializador.fromCSV(arquivoLidoReservas);
        GerenciadorQuarto gerente = new GerenciadorQuarto();
        gerente.setQuartos(quartos);
        return gerente ;
    }
    public Quarto CampoPraObjeto(){
        Quarto novoQuarto = new Quarto();
        novoQuarto.setNumeroQuarto(edtNomeQuarto.getText());
        novoQuarto.setTipo(etdTipoQuarto.getText());
        novoQuarto.setPrecoPorNoite(Float.parseFloat(edtPrecoQuarto.getText()));
        return novoQuarto;
    }
