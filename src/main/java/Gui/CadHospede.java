    private Hospede campoPraObjeto(){
        Hospede novoHospede = new Hospede();
        novoHospede.setContato(edtNumero.getText());
        novoHospede.setDocumentoIdentidade(edtIdentidade.getText());
        novoHospede.setEndereco(edtEndereco.getText());
        novoHospede.setNome(edtNomeHospede.getText());
        return novoHospede;
    }
    private void atualizarListagem() throws IOException{
        String listagem = "";
        List<Hospede> hospedes = gerente.getHospedes();
        for(Hospede hospede : hospedes){
            listagem = listagem + hospede.toString()+"\n";
        }
        exibirHospedes.setText(listagem);
        atualizarArquivo();
    }
    private void atualizarArquivo() throws IOException{
        SerializadorHospede serializador = new SerializadorHospede();
        String listaCSV = serializador.toCSV(gerente.getHospedes());
        FilePersistence file = new FilePersistence();
        file.saveToFile(listaCSV, "Hospedes.txt");
    }
