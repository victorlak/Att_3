    public GerenciadorHospede buscarGerente() throws FileNotFoundException{
        FilePersistence file = new FilePersistence();
        String arquivoLidoReservas = file.loadFromFile("Hospedes.txt");
        SerializadorHospede serializador = new SerializadorHospede();
        List<Hospede> hospedes = serializador.fromCSV(arquivoLidoReservas);
        GerenciadorHospede gerente = new GerenciadorHospede();
        gerente.setHospedes(hospedes);
        return gerente ;
    }

